// Qualquer número dividido por zero retorna infinty
console.log(7 / 0)

// String tem preferência então ele concatena, e + faz sentido no mundo da String
console.log('3' + 2)

/* Consigo fazer divisão de String por um number,
 * pois ele pega o conteúdo da String.
*/
console.log('10' / 2)

// NaN ao tentar múltiplcar um palavra em String
console.log('show' * 2)

// Certa imprecisão ao fazer cálculos muito pequenos, maior velocidade
console.log(0.1 + 0.7)

// Converter apra String precisa usar um ()
console.log((10.345).toString())
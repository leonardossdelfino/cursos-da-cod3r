// Procedural
processamento(valor1, valor2, valor3)

// OO
objeto = {
    valor1,
    valor2,
    valor3,
    processamento() {
        // ...
    }
}

objeto.processamento() // Foco passou a ser o objeto

/* 
    Princípios importantes OO
    
    1. Astração: 
    Abstrair informações de um obajeto "Real"

    2. Encapsulamento:
    "Esconder funções e atributos" separar em pequenos objetos

    3. Herança (prototype):
    Recebe atributos do objeto pai(JavaScript - único pai)

    4. Polimorfismo:
    Multiplas formas - Objeto carro (porde ser ferrari, fiat, wolks)
*/